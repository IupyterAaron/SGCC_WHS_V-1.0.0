package com.sgcc.entity;

public class Outlist {

  private long outlistid;
  private long outid;
  private long mtaid;
  private String outnum;
  private String unit;


  public long getOutlistid() {
    return outlistid;
  }

  public void setOutlistid(long outlistid) {
    this.outlistid = outlistid;
  }


  public long getOutid() {
    return outid;
  }

  public void setOutid(long outid) {
    this.outid = outid;
  }


  public long getMtaid() {
    return mtaid;
  }

  public void setMtaid(long mtaid) {
    this.mtaid = mtaid;
  }


  public String getOutnum() {
    return outnum;
  }

  public void setOutnum(String outnum) {
    this.outnum = outnum;
  }


  public String getUnit() {
    return unit;
  }

  public void setUnit(String unit) {
    this.unit = unit;
  }

}
